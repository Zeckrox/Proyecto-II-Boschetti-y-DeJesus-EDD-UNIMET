    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        if ("".equals(name.getText().replaceAll(" ", "") + lastName.getText().replaceAll(" ", ""))) {
            Alerta alerta = new Alerta("Introduzca la informaci√≥n del cliente.");
            alerta.setVisible(true);
        } else {
            Habitacion busqueda = (Habitacion) tablaClientes.get(name.getText().replaceAll(" ", "") + lastName.getText().replaceAll(" ", ""));
            if (busqueda == null) {
                Alerta alerta = new Alerta("Este cliente no se encuentra hospedado.");
                alerta.setVisible(true);
            } else {
                if (instruccion.equals("informacion")) {
                    InfoCliente ventanaBusqueda = new InfoCliente(busqueda, tablaClientes);
                    ventanaBusqueda.setVisible(true);
                    this.dispose();
                } else {
                    CheckOut checkoutWindow = new CheckOut(busqueda, tablaClientes);
                    checkoutWindow.setVisible(true);
                    this.dispose();
                }

            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

